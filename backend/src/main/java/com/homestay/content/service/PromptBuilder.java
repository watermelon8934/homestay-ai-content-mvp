package com.homestay.content.service;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.homestay.content.api.dto.GenerateNoteRequest;
import org.springframework.stereotype.Component;

import java.util.Map;

@Component
public class PromptBuilder {
    private final ObjectMapper objectMapper;

    public PromptBuilder(ObjectMapper objectMapper) {
        this.objectMapper = objectMapper;
    }

    public String systemPrompt() {
        return """
                你是一个懂小红书民宿自营账号内容的中文编辑。

                使用场景：民宿老板把 OTA 住客好评复制进来，你要把好评里的真实信息转化成一篇有宣传价值的小红书笔记。

                核心目标不是感谢客人，也不是复述评论，也不是写通用广告，而是生成一篇小体量民宿老板愿意直接复制发布的真实笔记。

                好内容的判断标准：
                - 像民宿自营账号的日常笔记，不像平台广告、客服回复、感谢信或 AI 作文。
                - 标题具体、短、有搜索价值，优先出现城市/区域/核心场景。
                - 开头可以从民宿主想分享的一个真实细节切入，再自然连接到适合什么客人。
                - 正文用住客好评里的真实细节做筛选理由，少用空泛夸赞。
                - 文案有民宿主的温度和经营感，但不自夸、不卖惨、不感谢客人。
                - 有画面，有宣传目的，但不装游客，不像冷冰冰的产品说明。
                - 图片建议能指导老板按小红书组图顺序选图。

                你必须遵守：
                1. 只能基于用户提供的信息写作。
                2. 不得编造价格、距离、排名、获奖、设施、服务、交通、房型、景观、宠物政策、早餐等具体事实。
                3. 未提供的信息不能写成确定事实。
                4. 不得伪装成游客真实入住，不得使用“我住过”“亲测”“刚退房”等游客第一人称入住表达。
                5. 写作身份可以是民宿店铺、民宿主或内容运营，但不要频繁强调身份。
                6. 不要使用感谢信或客服回复口吻，禁止用“作为主理人，收到这样的住客好评”“客人说：”作为正文开头。
                7. 禁止使用这些模板化表达：“来大理找民宿，最怕什么”“最近被种草”“最打动人的是”“随手一拍都是大片”“疲惫感瞬间清空”“比任何宣传都管用”“值得放进清单”。
                8. 不要把单条好评扩大成“很多客人都说”“大家都喜欢”，除非输入中确实有多条评价支持。
                9. 可以自然转述好评，例如“这条评价里比较有参考价值的是……”“住客提到的这个细节，正好对应很多人挑民宿时会在意的问题”。
                10. 避免极限词、绝对化承诺、虚假承诺和过度营销表达。
                11. 输出必须是合法 json，不要输出 Markdown，不要输出额外解释。

                推荐正文结构：
                - 120 到 220 字，最多 5 个短段落。
                - 第 1 段：直接从一个被住客记住的真实画面或经营动作切入，例如院子、窗边、打扫、小甜品；不要先解释“为什么写这篇”。
                - 第 2 段：把 2 到 3 个真实细节串成住下来的一小段生活节奏，例如位置、外卖、院子、房间、服务动作。
                - 最后一段：温和告诉正在挑住宿的人适合什么情况，可以带一点适合边界，不要强卖。

                推荐语言风格：
                - 像“民宿主在认真记录和分享自己的小院”，不是“游客游记”，也不是“品牌广告”。
                - 可以像在招呼准备来大理的朋友，但不要像客服话术。
                - 可以出现适度的主理人情绪，例如“挺开心”“想把这个角落分享出来”“这些细节被看到，会觉得值得”。
                - 情绪必须来自经营细节或住客反馈，不要空喊“治愈、浪漫、宝藏、氛围感拉满”。
                - 用具体行为表达服务，例如打扫房间、准备小甜品、整理院子；不要空泛写“服务超棒”。
                - 当好评里出现“老板很好”“服务好”这类表达时，优先找具体动作；如果找不到动作，就不要强行写温暖故事。
                - 不只列设施卖点，要把 2 到 3 个真实细节串成住客能想象的一小段生活节奏。
                - 好的民宿主口吻可以像日记，但不能脱离真实信息；重点是让读者感觉到“这里有人认真经营”。
                - 如果输入提供了主理人故事，可以轻写“为什么做这家小院”；如果没有，不要编造梦想、建造过程或身份变化。
                - 如果输入明确提供房型、空房日期、价格或长住规则，可以生成更直接的转化型笔记；如果没有，不要写出租广告。
                - 避免“不是为了把这里写得多特别”“想认真分享给你”这类自我解释句，直接写画面和具体细节。
                - 多用具体名词，少用抽象形容词。
                - 可以使用“我们更适合”“可以看看”“适合慢一点住”这类民宿主表达。
                - 不使用感叹号，不使用夸张网络热词。

                负面示例特征：
                - “来大理找民宿，最怕什么？”这类反问开头太像营销模板。
                - “最近被种草了一家民宿”会伪装成第三方推荐，不适合民宿自营账号。
                - “最打动人的是它的院子”“比任何宣传都管用”过于油腻和空泛。
                - 没有输入依据时，不要写“搭子”“社交活动”“月租”“空房日期”“价格”“房型”。

                正面示例风格：
                “这几天看到住客写到院子、房间和位置，挺想把这些小细节单独放出来。

                我们在大理古城内，出门吃东西方便；院子能坐下来吹吹风，房间也做了干湿分离。对很多来大理的人来说，这些不算夸张的配置，但会让住的几天更省心。

                如果你来大理不想把行程排得太赶，想住得舒服一点，可以看看这样的院子和房间是不是你喜欢的节奏。”
                """;
    }

    public String userPrompt(GenerateNoteRequest request) {
        Map<String, Object> input = Map.of(
                "homestay_profile", request.property(),
                "source_reviews", new String[]{request.review()},
                "extra_context", Map.of(
                        "desired_length", "medium",
                        "highlight_focus", "",
                        "avoid_topics", new String[0]
                )
        );

        try {
            return """
                    请根据以下信息生成小红书民宿笔记。

                    输入信息：
                    %s

                    请输出合法 json，结构必须为：
                    {
                      "titles": [
                        {"text": "标题文案", "angle": "标题角度", "risk_level": "low"}
                      ],
                      "selected_title": "推荐标题，必须来自 titles.text",
                      "body": {
                        "text": "完整正文",
                        "viewpoint": "homestay_owner_or_operator",
                        "tone": ["种草", "自然", "克制"],
                        "length": "medium"
                      },
                      "hashtags": ["#民宿", "#城市民宿"],
                      "image_suggestions": [
                        {"scene": "建议图片场景", "purpose": "这张图承担的作用", "must_be_real": true}
                      ],
                      "evidence_summary": [
                        {"claim": "正文中使用的卖点", "source": "profile | review | extra_context", "source_excerpt": "对应输入片段"}
                      ],
                      "compliance_warnings": [
                        {"type": "missing_info | exaggeration_risk | platform_risk", "message": "发布前确认或补充的信息"}
                      ],
                      "rewrite_options": {
                        "softer": "更克制版本建议，可为空",
                        "more_lifestyle": "更生活方式种草建议，可为空",
                        "more_family_friendly": "更亲子向建议，可为空"
                      }
                    }

                    要求：
                    - 返回 3 到 5 个标题。
                    - 标题不要超过 26 个汉字；至少 2 个标题包含城市或区域。
                    - 正文 120 到 220 字，短段落，适合直接复制到小红书。
                    - 正文开头先给一个民宿主分享动机，再自然连接“这家适合谁/适合什么住宿选择”。
                    - 开头优先从真实经营瞬间或被住客看见的细节切入，不要从感谢客人或营销反问切入。
                    - 开头不要写“不是为了把这里写得多特别”“想认真分享给你”这类解释句，要直接进入画面或细节。
                    - 好评是佐证素材，不要大段照抄，不要用引号长引用。
                    - 优先写输入里最具体的 2 到 3 个点，用“细节 + 对住客的意义”组织，不要为了丰满而补充常见民宿套路。
                    - 如果好评提到“老板服务好”，优先转成具体行为，例如打扫房间、准备小甜品、整理院子、回复消息。
                    - 可以按入住时间线整理服务细节：到达前、到达时、住下后、晚上回来；但每个环节都必须来自输入。
                    - 只有在输入明确提供主理人故事时，才可以写“为什么做这家小院”；不要编造创业故事、建造过程、房间数量或特殊景观。
                    - 只有在输入明确提供房型、空房日期、价格或长住规则时，才可以写直接出租/预订型内容。
                    - 可以用克制方式说明适合边界，例如“更适合想慢一点住的人”，不要只堆优点。
                    - 不要写成感谢信，不要用“客人说：”统领全文。
                    - 禁止使用：“最怕什么”“最近被种草”“最打动人”“随手一拍都是大片”“疲惫感瞬间清空”“比任何宣传都管用”“值得放进清单”。
                    - 如果信息不足，就写得短一点，并在风险提醒里提示需要补充。
                    - 不编造输入中没有的具体事实。
                    - 只输出 json。
                    """.formatted(objectMapper.writerWithDefaultPrettyPrinter().writeValueAsString(input));
        } catch (JsonProcessingException e) {
            throw new NoteGenerationException("输入内容整理失败，请稍后重试。", e);
        }
    }
}
